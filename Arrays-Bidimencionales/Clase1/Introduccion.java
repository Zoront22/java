public class Introduccion {
    // Este es un ejemplo de un array bidimensional en Java
    public static void main(String[] args) {
        // Declaracion de un array bidimensional de enteros
        int[][] arrayBidimensional;
        // numero de filas
        int numeroFilas = 3;
        // numero de columnas
        int numeroColumnas = 4;

        // Inicializacion del array bidimensional
        arrayBidimensional = new int[numeroFilas][numeroColumnas];
    }
}
